USE GAMERSHUB_AUTH;
GO

IF COL_LENGTH('dbo.USERS', 'CREATED_AT') IS NULL
BEGIN
  ALTER TABLE dbo.USERS
  ADD CREATED_AT DATETIME2 NULL;
END
GO

UPDATE dbo.USERS
SET CREATED_AT = COALESCE(CREATED_AT, SYSDATETIME())
WHERE CREATED_AT IS NULL;
GO

